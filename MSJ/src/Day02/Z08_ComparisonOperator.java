package Day02;

public class Z08_ComparisonOperator {

	public static void main(String[] args) {
        int x = 3;
        int y = 4;
        
        System.out.println((x == 3) && (y == 4)) ;
        System.out.println((x == 3) && (y == 7)) ;
        System.out.println((x == 3 || y == 4)) ;
        System.out.println((x != 3 || y == 4)) ;


	}

}
