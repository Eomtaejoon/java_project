package Day03;

public class Z06_Ifelself06 {

	public static void main(String[] args) {
		int a = 10, b = 20;
		
		if( a > b ) {
			System.out.println(a + ">" + b);
		}
		else if ( a < b ) {
			System.out.println(a + "<" + b);
		}
		else if ( a == b ){
			System.out.println(a + "==" + b);
		}

	}

}
