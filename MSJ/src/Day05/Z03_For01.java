package Day05;
public class Z03_For01 {

	public static void main(String[] args) {

		for(int i=0; i<=10; i++ )
		{
			if( i%2 == 0)
				System.out.println(i);			
		}
		System.out.println();
		
		for(int i=10; i>0; i-- )
		{
			if( i%2 == 0)
				System.out.println(i);			
		}

	}

}
